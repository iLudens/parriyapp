def generate_slug(title):
    title_slug = title.lower().replace(' ', '-')

    array = [':', '!', '?', ',', '_', '¡', '¿', ';', '/', '.', '(', ')', '[', ']', '{', '}', '’', '‘', '“', '”',
             '–', '—', '…', '•', '°', '´', '¨', '·', '«', '»', '©', '®', '™', '§', '¶', '†', '‡', '±', '≠', '≈', '≤', '≥', '∞',
             '∂', '∑', '∏', '∫', '√', '∆', '√', '∫', '∏', '∑', '∂', '∞', '≥', '≤', '≈', '≠', '±', '‡', '†', '¶', '§', '™', '®', '©',
             '»', '«', '·', '¨', '´', '°', '•', '…', '—', '–', '”', '“', '‘', '’', '}', '{', ']', '[', ')', '(', '.', '/', ';', '¿', '¡',
             '_', ',', '?', '!', ':']

    for char in array:
        if char in title_slug:
            title_slug = title_slug.replace(char, '')

    return title_slug
